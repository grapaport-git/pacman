export const LEVELS = [LEVEL_1, LEVEL_2];

// Generate level 3-15 with slight maze variations using seed
export function generateLevel(n) {
  if (n < 3) return LEVELS[n - 1];
  const base = LEVEL_1.map(row => [...row]);
  // Procedurally flip some paths for variety
  const seed = n * 17 + 13;
  for (let i = 0; i < (seed % 4); i++) {
    const x = 3 + (i * 5) % 20;
    const y = 5 + (i * 7) % 20;
    if (base[y] && base[y][x] === 1) base[y][x] = 0;
  }
  return base;
}
