export function getTile(x, y) {
  if (y < 0 || y >= MAZE_DATA.length) return 1;
  if (x < 0 || x >= MAZE_DATA[0].length) {
    if (isTunnel(x, y)) return 0;
    return 1;
  }
  return MAZE_DATA[y][x];
}

export function isWall(x, y) {
  const tile = getTile(x, y);
  return tile === 1;
}

export function isTunnel(x, y) {
  const h = MAZE_DATA.length;
  const w = MAZE_DATA[0].length;
  return y === 14 && (x < 0 || x >= w);
}

export function getMazeWidth() {
  return MAZE_DATA[0].length;
}

export function getMazeHeight() {
  return MAZE_DATA.length;
}

export function countDots() {
  let count = 0;
  for (let row of MAZE_DATA) {
    for (let cell of row) {
      if (cell === 2) count++;
    }
  }
  return count;
}

export function countPowerPellets() {
  let count = 0;
  for (let row of MAZE_DATA) {
    for (let cell of row) {
      if (cell === 3) count++;
    }
  }
  return count;
}
